package com.github.seafalcon;

public class Item {
    private int magicalOffensive = 0;
    private int physicalOffensive = 0;
    private int getHp = 0;
    private int magicalDefensive = 0;
    private int physicalDefensive = 0;

    public int getMagicalOffensive() {
        return magicalOffensive;
    }

    public void setMagicalOffensive(int magicalOffensive) {
        this.magicalOffensive = magicalOffensive;
    }

    public int getPhysicalOffensive() {
        return physicalOffensive;
    }

    public void setPhysicalOffensive(int physicalOffensive) {
        this.physicalOffensive = physicalOffensive;
    }

    public int getHp() {
        return getHp;
    }

    public void setHp(int hp) {
        this.getHp = hp;
    }

    public int getMagicalDefensive() {
        return magicalDefensive;
    }

    public void setMagicalDefensive(int magicalDefensive) {
        this.magicalDefensive = magicalDefensive;
    }

    public int getPhysicalDefensive() {
        return physicalDefensive;
    }

    public void setPhysicalDefensive(int physicalDefensive) {
        this.physicalDefensive = physicalDefensive;
    }
}
